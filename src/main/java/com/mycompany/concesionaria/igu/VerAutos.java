    public void cargarTabla(){
        DefaultTableModel tabla = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row, int col){
                return false;
            }
        };
        String titulos[] = {"ID", "Modelo", "Color", "Patente", "Cantidad de Puertas"};
        tabla.setColumnIdentifiers(titulos);
        
        List<Auto> listaAutos = control.obtenerAutos();
        
        if (listaAutos != null) {
            for (Auto auto: listaAutos) {
                Object[] objeto = {auto.getId(), auto.getModelo(), auto.getColor(), auto.getPatente(), auto.getCantidadPuertas()};
                
                tabla.addRow(objeto);
            }
        }
        tablaAutos.setModel(tabla);
    }
    
    public void mostrarMensaje(String mensaje, String titulo, String tipo) {
        JOptionPane optionPane = new JOptionPane(mensaje);
        if (tipo.equals("info")) {
            optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        } else {
            optionPane.setMessageType(JOptionPane.ERROR_MESSAGE);
        }
        JDialog dialog = optionPane.createDialog(titulo);
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);
    }
