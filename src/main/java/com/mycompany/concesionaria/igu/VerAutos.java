    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if(tablaAutos.getRowCount() > 0){
            if(tablaAutos.getSelectedRow() != -1){
                int id = Integer.parseInt(String.valueOf(tablaAutos.getValueAt(tablaAutos.getSelectedRow(), 0)));
                control.eliminarAuto(id);
                mostrarMensaje("Se elimino correctamente", "Eliminacion exitosa", "info");
                cargarTabla();
            } else {
                mostrarMensaje("No hay fila seleccionada", "Error Eliminacion", "error");
            }
        } else {
            mostrarMensaje("La tabla esta vacia", "Error Eliminacion", "error");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if(tablaAutos.getRowCount() > 0){
            if(tablaAutos.getSelectedRow() != -1){
                int id = Integer.parseInt(String.valueOf(tablaAutos.getValueAt(tablaAutos.getSelectedRow(), 0)));
                EditarAuto pantalla = new EditarAuto(id);
                pantalla.setVisible(true);
                pantalla.setLocationRelativeTo(null);
                this.dispose();
            } else {
                mostrarMensaje("No hay fila seleccionada", "Error Edicion", "error");
            }
        } else {
            mostrarMensaje("La tabla esta vacia", "Error Edicion", "error");
        }
    }//GEN-LAST:event_btnEditarActionPerformed


